#include <iostream>
using namespace std;

int RepeatedPrinting(int n)
{
    for(int i = 0; i < n; i++)
    {
        cout<<"Hello Coder Army"<<endl;
    }
}


int main()
{
   
   int num;

   cout<<"Enter the number: ";
   cin>>num;

   RepeatedPrinting(num);

}